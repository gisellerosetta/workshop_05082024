def add_one(number_to_add: int):
    """
    Parameters
    ----------
    number_to_add
        The number to add 1 to.

    Returns
    -------
    int
        number_to_add + 1
    """
    return(number_to_add + 1)

def add_two(number_to_add: int):
    """
    Adds two to a number.
    """
    return(number_to_add + 2)